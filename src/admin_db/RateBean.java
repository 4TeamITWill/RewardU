package admin_db;

public class RateBean {
	String user_id;
	int pd_no;
	double my_rate;
	
	public String getUser_id() {
		return user_id;
	}
	public void setUser_id(String user_id) {
		this.user_id = user_id;
	}
	public int getPd_no() {
		return pd_no;
	}
	public void setPd_no(int pd_no) {
		this.pd_no = pd_no;
	}
	public double getMy_rate() {
		return my_rate;
	}
	public void setMy_rate(double my_rate) {
		this.my_rate = my_rate;
	}
}
